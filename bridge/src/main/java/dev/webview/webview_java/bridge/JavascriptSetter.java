package dev.webview.webview_java.bridge;

import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Like Javascript's set: function feature, this allows you to call
 * Example.property = 123 and execute a method in Java rather than needing to
 * call Example.setProperty(123).
 */
@Retention(RUNTIME)
@Target(METHOD)
public @interface JavascriptSetter {

    String value();

}
